function lbv_hint_definition(string $key): array {
  $defs = lbv_hint_definitions();
  return $defs[$key] ?? $defs['evaluation'];
}

function lbv_hint_ref(string $key): string {
  return (string)(lbv_hint_definition($key)['paragraph'] ?? '');
}

function lbv_hint_short_hint(string $key): string {
  return (string)(lbv_hint_definition($key)['short_hint'] ?? '');
}

function lbv_hint_title(string $key): string {
  return (string)(lbv_hint_definition($key)['title'] ?? '');
}

function lbv_hint_escape(string $text): string {
  return htmlspecialchars($text, ENT_QUOTES, 'UTF-8');
}

function lbv_hint_links_html(string $key): string {
  $links = lbv_hint_definition($key)['links'] ?? [];
  if (!is_array($links) || !$links) return '';
  $parts = [];
  foreach ($links as $link) {
    if (!is_array($link)) continue;
    $label = trim((string)($link['label'] ?? ''));
    $url = trim((string)($link['url'] ?? ''));
    if ($label === '' || $url === '') continue;
    $parts[] = '<a href="' . lbv_hint_escape($url) . '" target="_blank" rel="noopener noreferrer">' . lbv_hint_escape($label) . '</a>';
  }
  if (!$parts) return '';
  return '<div class="muted"><b>Gesetzestext:</b> ' . implode(' · ', $parts) . '</div>';
}

function lbv_hint_card_html(string $key, bool $show_heading = false): string {
  $hint = lbv_hint_definition($key);
  $html = '';
  if ($show_heading) {
    $html .= '<div class="muted"><b>' . lbv_hint_escape((string)$hint['title']) . ' (' . lbv_hint_escape((string)$hint['paragraph']) . ')</b></div>';
    $html .= '<div style="height:8px"></div>';
  }

  $sections = [
    'Worum es geht' => (string)$hint['summary'],
    'Für die Beurteilung' => (string)$hint['assessment_guidance'],
    'Dokumentation' => (string)$hint['documentation_hint'],
  ];

  $parts = [];
  foreach ($sections as $label => $text) {
    $parts[] = '<div class="muted"><b>' . lbv_hint_escape($label) . ':</b> ' . lbv_hint_escape($text) . '</div>';
  }
  $links_html = lbv_hint_links_html($key);
  if ($links_html !== '') $parts[] = $links_html;

  return $html . implode('<div style="height:8px"></div>', $parts);
}

function lbv_hint_group_html(array $keys): string {
  $parts = [];
  foreach ($keys as $key) {
    $parts[] = lbv_hint_card_html($key, true);
  }
  return implode('<div style="height:14px"></div>', $parts);
}

function written_assessment_types(): array {
  return [
    'SA' => 'Schularbeit',
    'TEST' => 'Test',
    'REVIEW' => 'Schriftliche Wiederholung',
    'TASK' => 'Arbeitsauftrag',
    'OTHER' => 'Sonstige schriftliche Leistung',
  ];
}

function written_assessment_normalize_type(string $type): string {
  $type = strtoupper(trim($type));
  if ($type === '') return 'SA';
  return array_key_exists($type, written_assessment_types()) ? $type : 'OTHER';
}

function written_assessment_type_label(string $type): string {
  $type = written_assessment_normalize_type($type);
  $types = written_assessment_types();
  return $types[$type];
}

function written_assessment_type_short_label(string $type): string {
  $type = written_assessment_normalize_type($type);
  $map = [
    'SA' => 'SA',
    'TEST' => 'Test',
    'REVIEW' => 'Wdh.',
    'TASK' => 'Auftrag',
    'OTHER' => 'Sonst.',
  ];
  return $map[$type] ?? $type;
}

function written_assessment_lbv_key(string $type): string {
  $type = written_assessment_normalize_type($type);
  return $type === 'SA' ? 'written_exam' : 'test';
}

function written_assessment_summary(string $type): string {
  $raw = strtoupper(trim($type));
  if ($raw === 'ALL') return lbv_hint_group_html(['written_exam', 'test']);
  $type = written_assessment_normalize_type($raw);
  if ($type === 'SA') return lbv_hint_card_html('written_exam');
  return lbv_hint_card_html('test');
}

function written_assessment_summary_ref(string $type): string {
  $raw = strtoupper(trim($type));
  if ($raw === 'ALL') return '§ 7 und § 8 LBV';
  $type = written_assessment_normalize_type($raw);
  if ($type === 'SA') return lbv_hint_ref('written_exam');
  return lbv_hint_ref('test');
}

