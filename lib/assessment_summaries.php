function written_assessment_summary_tooltip(string $type): string {
  $raw = strtoupper(trim($type));
  if ($raw === 'ALL') {
    return lbv_hint_title('written_exam') . ': ' . lbv_hint_short_hint('written_exam') . ' | '
      . lbv_hint_title('test') . ': ' . lbv_hint_short_hint('test');
  }
  $type = written_assessment_normalize_type($raw);
  if ($type === 'SA') return lbv_hint_short_hint('written_exam');
  return lbv_hint_title('test') . ': ' . lbv_hint_short_hint('test');
}

